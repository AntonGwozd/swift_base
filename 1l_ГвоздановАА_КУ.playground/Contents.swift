import Foundation

print("Решаем геометрические задачи с прямоугольным треугольников")

// Первый катет
print("Введите длину первого катета:")
var str1 = readLine()
let k1 = Double(str1!)
if k1 == 0 || k1 == nil {
    print("не допустимое значение, работа программы прервана")
} else {
    //второй катет
    print("Введите длину первого катета:")
    str1 = readLine()
    let k2 = Double(str1!)
    if k2 == 0 || k2 == nil {
        print("не допустимое значение, работа программы прервана")
    } else {
        //1 задание площадь
        let s = (k1!*k2!)/2
        //3 задание, а без него 2 не сделаешь, гипотенуза
        let g = sqrt(k1!*k1!+k2!*k2!)
        // 2 задание периметр
        let p = k1!+k2!+g
        print ("Площадь треугольника: "+String(s))
        print ("Гипотенуза треугольника: "+String(g))
        print ("Периметр треугольника: "+String(p))
    }
}
